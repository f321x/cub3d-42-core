#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	size_t	string_length;

	string_length = ft_strlen(s);
	i = string_length;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(&s[i]));
		i--;
	}
	return (NULL);
}
